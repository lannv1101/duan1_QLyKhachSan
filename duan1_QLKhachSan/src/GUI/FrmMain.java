    void showtime() {
        new Timer(0, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date d = new Date();
                SimpleDateFormat s = new SimpleDateFormat("hh:mm:ss a");
                SimpleDateFormat D = new SimpleDateFormat("dd/MM/yyyy");
                lbltime.setText(s.format(d) + "-" + D.format(d));
            }
        }).start();
    }

    void showHideSubMenu(int so, JPanel panel) {
        so++;
        if (so % 2 == 0) {
            pnlSubNhanVien.setVisible(true);
            pnlSubKhachHang.setVisible(true);
            pnlSubPhong.setVisible(true);
            pnlSubTaiKhoan.setVisible(true);
            pnlSubCaiDat.setVisible(true);

        } else {
            pnlSubNhanVien.setVisible(false);
            pnlSubKhachHang.setVisible(false);
            pnlSubPhong.setVisible(false);
            pnlSubTaiKhoan.setVisible(false);
            pnlSubCaiDat.setVisible(false);
        }
        JPanel[] pnl = new JPanel[]{pnlSubCaiDat, pnlSubKhachHang, pnlSubNhanVien, pnlSubPhong, pnlSubTaiKhoan};
        for (int i = 0; i < pnl.length; i++) {
            pnl[i].setVisible(false);
        }
        panel.setVisible(true);
    }

    void hover(int so, JPanel pnl) {
        if (so == 1) {
            pnl.setBackground(new Color(13, 78, 144));
            pnl.setCursor(new Cursor(HAND_CURSOR));

        } else {
            pnl.setBackground(new Color(0, 34, 68));

        }
    }

