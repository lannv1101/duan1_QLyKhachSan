    private void btnThongkeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThongkeActionPerformed
        // TODO add your handling code here:
        try {
            tab.setSelectedComponent(pnl_ThongKe.form);
        } catch (Exception e) {
            tab.removeAll();
            pnl_ThongKe pnl = new pnl_ThongKe();
            tab.add("Thống kê doanh thu", pnl);
            tab.setSelectedComponent(pnl);
        }
        pnlSubNhanVien.setVisible(false);
        pnlSubKhachHang.setVisible(false);
        pnlSubPhong.setVisible(false);
        pnlSubTaiKhoan.setVisible(false);
        pnlSubCaiDat.setVisible(false);
    }//GEN-LAST:event_btnThongkeActionPerformed

