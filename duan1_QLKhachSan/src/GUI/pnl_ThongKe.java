    public void setDataToChart1() {
        List<DTO.DTO_HoaDon> listItem = thongkeservice.getlistbyhoadon();

        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        if (listItem != null) {
            for (DTO_HoaDon item : listItem) {
                dataset.addValue((Number) item.getThanhTien(), "Mã Hóa Đơn", item.getMaDP());
            }
        }

        JFreeChart barChart = ChartFactory.createBarChart(
                "Biểu Đồ Thống Kê Hóa Đơn".toUpperCase(),
                "Mã Hóa Đơn", "Thành Tiền",
                dataset, PlotOrientation.VERTICAL, false, true, false);

        ChartPanel chartPanel = new ChartPanel(barChart);
        chartPanel.setPreferredSize(new Dimension(pnl_bieudoTK.getWidth(), 500));

        pnl_bieudoTK.removeAll();
        pnl_bieudoTK.setLayout(new CardLayout());
        pnl_bieudoTK.add(chartPanel);
        pnl_bieudoTK.validate();
        pnl_bieudoTK.repaint();
    }

