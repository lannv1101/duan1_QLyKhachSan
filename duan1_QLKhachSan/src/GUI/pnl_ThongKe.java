    private void btn_InPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_InPDFActionPerformed
        // TODO add your handling code here:
        // XSSFWorkbook wb = new XSSFWorkbook();
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if (x == JFileChooser.APPROVE_OPTION) {
            path = j.getSelectedFile().getPath();
        }

        Document doc = new Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path + "Thongkeluong.pdf"));

            doc.open();
            PdfPTable tbl = new PdfPTable(5);
            tbl.addCell("Tên Nhân Viên");
            tbl.addCell("Số Ngày Nghỉ");
            tbl.addCell("Số Ngày Làm");
            tbl.addCell("Ngày Nhận");
            tbl.addCell("Tổng Nhận");

            for (int i = 0; i < tbl_Luong.getRowCount(); i++) {

                String tennv = tbl_Luong.getValueAt(i, 0).toString();
                String snn = tbl_Luong.getValueAt(i, 1).toString();
                String snl = tbl_Luong.getValueAt(i, 2).toString();
                String nn = tbl_Luong.getValueAt(i, 3).toString();
                String tn = tbl_Luong.getValueAt(i, 4).toString();

                tbl.addCell(tennv);
                tbl.addCell(snn);
                tbl.addCell(snl);
                tbl.addCell(nn);
                tbl.addCell(tn);
            }
            doc.add(tbl);
            JOptionPane.showMessageDialog(null, "Đang tải PDF", "Thông Báo", JOptionPane.WARNING_MESSAGE);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(pnl_ThongKe.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(pnl_ThongKe.class.getName()).log(Level.SEVERE, null, ex);
        }
        doc.close();

    }//GEN-LAST:event_btn_InPDFActionPerformed
    private static XSSFCellStyle createStyleForTitle(XSSFWorkbook workbook) {
        XSSFFont font = workbook.createFont();
        font.setBold(true);
        XSSFCellStyle style = workbook.createCellStyle();
        style.setFont(font);
        font.setColor(IndexedColors.RED.getIndex());
        font.setBold(true);

        return style;
    }
    private void btn_InexcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_InexcelActionPerformed
        // TODO add your handling code here:
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if (x == JFileChooser.APPROVE_OPTION) {
            path = j.getSelectedFile().getPath();
        }
        try {
            XSSFWorkbook workbook = new XSSFWorkbook();
            XSSFSheet spreadsheet = workbook.createSheet("Thống Kê Lương");
            XSSFCellStyle style = createStyleForTitle(workbook);

            XSSFRow row = null;
            Cell cell = null;

            row = spreadsheet.createRow((short) 2);
            row.setHeight((short) 500);
            cell = row.createCell(0, CellType.STRING);
            cell.setCellValue("Danh Sách Lương");
            row = spreadsheet.createRow((short) 3);
            row.setHeight((short) 500);
            cell = row.createCell(0, CellType.STRING);
            cell.setCellValue("Tên Nhân Viên");
            cell.setCellStyle(style);

            cell = row.createCell(1, CellType.STRING);
            cell.setCellValue("Số Ngày Nghỉ");
            cell.setCellStyle(style);

            cell = row.createCell(2, CellType.STRING);
            cell.setCellValue("Số Ngày Làm");
            cell.setCellStyle(style);
            cell = row.createCell(3, CellType.STRING);
            cell.setCellValue("Ngày Nhận");
            cell.setCellStyle(style);
            cell = row.createCell(4, CellType.STRING);
            cell.setCellValue("Tổng Nhận");
            cell.setCellStyle(style);

            for (int i = 0; i < tbl_Luong.getRowCount(); i++) {
                row = spreadsheet.createRow((short) 4 + i);

                row.setHeight((short) 400);

                row.createCell(0).setCellValue(tbl_Luong.getValueAt(i, 0).toString());
                row.createCell(1).setCellValue(tbl_Luong.getValueAt(i, 1).toString());
                row.createCell(2).setCellValue(tbl_Luong.getValueAt(i, 2).toString());
                row.createCell(3).setCellValue(tbl_Luong.getValueAt(i, 3).toString());
                row.createCell(4).setCellValue(tbl_Luong.getValueAt(i, 4).toString());

            }
            FileOutputStream out = new FileOutputStream(path + "Thongkeluongexcel.xlsx");
            workbook.write(out);

            out.close();
            JOptionPane.showMessageDialog(null, "Đang tải File Excel", "Thông Báo", JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_btn_InexcelActionPerformed


